package package1;

public class Datatypes {

	public static void main(String[] args) {
		byte a=23;
		System.out.println("Byte code of a="+a);
		short sh=231;
		System.out.println("Short Value="+sh);
		int b=35;
		System.out.println("Integer Value="+b);
		long c=233455322l;
		System.out.println("Long Value="+c);
		boolean bl=30>40;
		System.out.println("Boolean Value="+bl);
		float fl=4.3455f;
		System.out.println("Float Value="+fl);
		double d=4.53;
		System.out.println("Double Value="+d);
	char ch='a';
		System.out.println("Character="+ch);
		String s="hai";
		System.out.println("String="+s);
		
		

	}

}
